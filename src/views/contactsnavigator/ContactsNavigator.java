  private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnDeleteActionPerformed
    int option = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir o contato?", "Agenda",
        JOptionPane.YES_NO_OPTION);

    if (option != JOptionPane.YES_OPTION)
      return;

    try {
      contactController.deleteContact(selectedContact.getId());
      JOptionPane.showMessageDialog(null, "Contato excluído com sucesso", getTitle(), JOptionPane.INFORMATION_MESSAGE);
      cmbChangeContact.removeItem(selectedContact.getName());

      int deletedContactIndex = FindContact.findIndexOfContactByName(selectedContact.getName(), contacts);

      contacts.remove(deletedContactIndex);
      actualContactIndex = deletedContactIndex != 0 ? deletedContactIndex - 1 : 0;
      updateSelectedContact();
    } catch (Exception e) {
      System.err.println(e);
      JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir o contato", "Erro", JOptionPane.ERROR_MESSAGE);
    }
  }// GEN-LAST:event_btnDeleteActionPerformed

