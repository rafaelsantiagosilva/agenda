  private void cmbChangeContactActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_cmbChangeContactActionPerformed
    String name = cmbChangeContact.getSelectedItem().toString();
    actualContactIndex = FindContact.findIndexOfContactByName(name, contacts);
    updateSelectedContact();
  }// GEN-LAST:event_cmbChangeContactActionPerformed

  private void btnPreviousActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnPreviousActionPerformed
    if (actualContactIndex != 0) {
      actualContactIndex--;
      updateSelectedContact();
    }
  }// GEN-LAST:event_btnPreviousActionPerformed

  private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnNextActionPerformed
    if (actualContactIndex != contacts.size() - 1) {
      actualContactIndex++;
      updateSelectedContact();
    }
  }// GEN-LAST:event_btnNextActionPerformed

